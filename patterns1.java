package com.te.javabasics.common;

import java.util.Scanner;

public class patterns1 {
	public static void main(String[] args) {
		for (char i = 'A'; i == 'A'; i++) {
			System.out.println(i);

		}
		for (char i = 'B'; i >= 'A'; i--) {
			System.out.print(i + " ");
			if (i == 'A') {
				System.out.println(" ");
			}
		}
		for (char i = 'C'; i >= 'A'; i--) {
			System.out.print(i + " ");
			if (i == 'A') {
				System.out.println(" ");
			}
		}

		for (char i = 'D'; i >= 'A'; i--) {
			System.out.print(i + " ");
		}
	}
}
